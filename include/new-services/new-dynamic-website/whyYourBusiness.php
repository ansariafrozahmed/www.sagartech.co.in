                const percentY = (y / rect.height - 0.5) * 2;

                gsap.to(card, {
                    rotateX: -percentY * 10,
                    rotateY: percentX * 10,
                    transformPerspective: 1000,
                    transformOrigin: "center",
                    duration: 0.3,
                    ease: "power2.out"
                });
            });
        });
