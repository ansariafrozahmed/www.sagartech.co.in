<script src="https://cdn.jsdelivr.net/npm/gsap@3.12.5/dist/ScrollTrigger.min.js"></script>
<script src="https://cdn.jsdelivr.net/npm/split-type@0.3.4/dist/split-type.umd.js"></script>

<script>
    // Register ScrollTrigger plugin
    gsap.registerPlugin(ScrollTrigger);

    // 1. Split heading text into characters
    const splitHeading = new SplitType("#animated-heading", {
        types: "words, chars"
    });

    // 2. Preserve word spacing
    splitHeading.words.forEach(word => {
        word.style.display = "inline-block";
        word.style.whiteSpace = "nowrap";
    });

    // 3. Create a timeline for animations
    const headingTimeline = gsap.timeline({
        scrollTrigger: {
            trigger: "#animation-bottom",
            start: "top 40%",
            end: "top 12%",
            scrub: 1.2,
            pin: false,
            markers: false
        }
    });

    // 4. Animate heading characters
    headingTimeline.fromTo(
        splitHeading.chars, {
            opacity: 0,
            filter: "blur(4px)"
        }, {
            opacity: 1,
            filter: "blur(0px)",
            stagger: 0.05,
            ease: "power4.out"
        }
    );

    // 5. Animate subtitle
    headingTimeline.fromTo(
        "#animated-subtitle", {
            opacity: 0,
            y: 20
        }, {
            opacity: 1,
            y: 0,
            duration: 0.6,
            ease: "power2.out"
        }
    );

    // 6. Animate paragraph
    headingTimeline.fromTo(
        "#animated-paragraph", {
            opacity: 0,
            y: 20
        }, {
            opacity: 1,
            y: 0,
            duration: 0.6,
            ease: "power2.out"
        }
    );

    // 9. Card animations (only for screens >= 768px)
    if (window.innerWidth >= 768) {
        const cards = gsap.utils.toArray("#grid-column-container > div");

        const timeline = gsap.timeline({
            scrollTrigger: {
                trigger: "#grid-column-container",
                start: "top 20%",
                end: "+=1200",
                pin: true,
                scrub: 1.5
            }
        });

        cards.forEach((card) => {
            timeline.from(card, {
                x: 400,
                opacity: 0,
                duration: 0.6,
                ease: "power2.out"
            }, ">");
        });
    }
</script>

<script>
    // Define your default body background
    const defaultBodyBg = "#141414";

    ScrollTrigger.create({
        trigger: "#main-section",
        start: "top 40%",
        end: "bottom 20%",
        scrub: true,
        onEnter: () => {
            document.body.style.backgroundColor = "#ffffff"; // Set to white
            updateMainSectionText("light");
        },
        onEnterBack: () => {
            document.body.style.backgroundColor = "#ffffff";
            updateMainSectionText("light");
        },
        onLeave: () => {
            document.body.style.backgroundColor = defaultBodyBg; // Revert to dark
            updateMainSectionText("dark");
        },
        onLeaveBack: () => {
            document.body.style.backgroundColor = defaultBodyBg;
            updateMainSectionText("dark");
        }
    });

    function updateMainSectionText(mode) {
        const heading = document.getElementById("animated-heading");
        const subtitle = document.getElementById("animated-subtitle");
        const paragraph = document.getElementById("animated-paragraph");

        if (mode === "light") {
            heading.style.color = "#242424";
            subtitle.style.color = "#4b5563";
            paragraph.style.color = "#6b7280";
        } else {
            heading.style.color = "#ffffff";
            subtitle.style.color = "#d1d5db";
            paragraph.style.color = "#f3f4f6";
        }
    }

    // Smooth transition
    document.body.style.transition = "background-color 0.5s ease";
</script>